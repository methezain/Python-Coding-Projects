# 'pascal case' VS 'camel case' VS 'snack case'


# PASCAL CASE
## every word starts with capital letter => AliZain, VariableName, UserClass, CountDown. 
### the names of the classes are written in pascal case.


# CAMEL CASE 
## every word starts with capital letter except the 1st one. => aliZain, variableName, methodName, userClass, countDown. 
## variable names + method names are written in camel case. 


# SNACK CASE 
## lower cases with under_scores. => ali_zain, variable_name, method_name, user_class. 
### anything can be written except classes. 